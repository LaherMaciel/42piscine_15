#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	i;

	i = 0;
	if (nb < 0)
	{
		write (1, "-", 1);
		if (nb <= -2147483648)
		{
			write (1, "2", 1);
			ft_putnbr(147483648);
			return ;
		}
		nb = -nb;
	}
	if (nb < 10)
	{
		nb += 48;
		write (1, &nb, 1);
	}
	else
	{
		ft_putnbr(nb / 10);
		i = (nb % 10) + 48;
		write (1, &i, 1);
	}
}
