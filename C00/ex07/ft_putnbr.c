#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	i;

	i = 0;
	if (nb < 10)
	{
		nb += 48;
		write (1, &nb, 1);
	}
	else
	{
		ft_putnbr(nb / 10);
		i = (nb % 10) + 48;
		write (1, &i, 1);
	}
}
